package com.fasset.form;

public class ExempListForm {
	private Long supId;
	private String desc;
	private Double nillRated;
	private Double exempted;
	private Double nonGst;
	
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public Double getNillRated() {
		return nillRated;
	}
	public void setNillRated(Double nillRated) {
		this.nillRated = nillRated;
	}
	public Double getExempted() {
		return exempted;
	}
	public void setExempted(Double exempted) {
		this.exempted = exempted;
	}
	public Double getNonGst() {
		return nonGst;
	}
	public void setNonGst(Double nonGst) {
		this.nonGst = nonGst;
	}
	public Long getSupId() {
		return supId;
	}
	public void setSupId(Long supId) {
		this.supId = supId;
	}	

}
